/////////////////////////////////////////////////
/// @file
/// @brief Declaration for the ButtonStyle struct used for defining button
/// styles
/////////////////////////////////////////////////

/////////////////////////////////////////////////
/// Preprocessor Directives
/////////////////////////////////////////////////
#pragma once

/////////////////////////////////////////////////
/// Headers
/////////////////////////////////////////////////
#include "Style.h"
#include <memory>

struct ButtonStyle : public Style {
  /////////////////////////////////////////////////
  /// @brief Fill color of the text
  /////////////////////////////////////////////////
  sf::Color text_color;

  /////////////////////////////////////////////////
  /// @brief Fill color of the background when hovered
  /////////////////////////////////////////////////
  sf::Color hover_color;

  /////////////////////////////////////////////////
  /// @brief Shared pointer to the font used for the button text
  /////////////////////////////////////////////////
  std::shared_ptr<const sf::Font> font{nullptr};

  /////////////////////////////////////////////////
  /// @brief Font size used for the button text
  /////////////////////////////////////////////////
  uint8_t font_size;
};
